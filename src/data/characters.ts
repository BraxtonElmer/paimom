export const getCharacter = (name: string): Character | null => {
  const key = name.toLowerCase().replace(/\s+/g, '').replace(/[()]/g, '');
  return characters[key] || null;
};

export const getAllCharacterNames = (): string[] => {
  return Object.values(characters).map(char => char.name);
};

export const getCharactersByElement = (element: string): Character[] => {
  return Object.values(characters).filter(char => char.element === element);
};
